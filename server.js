const normalizedAliases = {};

Object.entries(aliases).forEach(([key, value]) => {
    const normalizedKey = key.trim().replace(/\s+/g, ' ').toLowerCase();
    const normalizedValue = value.trim().replace(/\s+/g, ' ').toLowerCase();

    normalizedAliases[normalizedKey] = value;
    if (!normalizedAliases[normalizedValue]) {
        normalizedAliases[normalizedValue] = value;
    }
});

function getCollegeFullName(input) {
    if (!input || typeof input !== 'string') return input;
    const normalizedInput = input.trim().replace(/\s+/g, ' ').toLowerCase();
    return normalizedAliases[normalizedInput] || input;
}
