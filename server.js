// دالة محسنة لتحليل السؤال والرد
// دالة محسنة لتحليل السؤال والرد
function answerQuestion(question) {
    const q = question.toLowerCase().replace(/\s+/g, ' ').trim();

    // 1. استخراج اسم الكلية من السياق باستخدام المرادفات
    let matchedColleges = [];
    let collegeName = null;

    // البحث في مرادفات الكليات
    for (const [alias, fullName] of Object.entries(aliases)) {
        const normalizedAlias = alias.toLowerCase();
        if (q.includes(normalizedAlias)) {
            collegeName = fullName;
            break;
        }
    }

    // إذا لم نجد مرادف، نستخدم البحث الضبابي
    if (!collegeName) {
        const collegeKeywords = Object.keys(aliases);
        const fuseCollege = new Fuse(collegeKeywords, { threshold: 0.4 });
        const collegeResults = fuseCollege.search(q);

        if (collegeResults.length > 0) {
            collegeName = aliases[collegeResults[0].item];
        }
    }

    // إذا وجدنا اسم الكلية، نبحث عنها في القاعدة
    if (collegeName) {
        matchedColleges = colleges.filter(c =>
            c.name_ar.toLowerCase().includes(collegeName.toLowerCase())
        );
    }

    // إذا لم نجد أي كلية بعد
    if (matchedColleges.length === 0) {
        // محاولة الإجابة على سؤال عام بدون ذكر كلية
        return answerGeneralQuestion(q);
    }

    // 2. البحث عن الحقول بدقة
    let matchedField = null;

    for (const item of fieldMappings) {
        // تطبيع الحقول والمرادفات
        const normalizedSynonyms = item.synonyms.map(s =>
            s.toLowerCase().replace(/[؟?]/g, '')
        );

        // البحث عن أي مرادف في السؤال
        if (normalizedSynonyms.some(syn => q.includes(syn))) {
            matchedField = item;
            break;
        }
    }

    if (!matchedField) {
        const collegeNames = matchedColleges.map(c => c.name_ar).join('، ');
        return `❌ لم أفهم ما الذي تريد معرفته عن ${collegeNames}. يرجى صياغة السؤال بشكل أوضح.`;
    }

    // 3. معالجة الردود
    return generateResponse(matchedColleges, matchedField);
}

// دالة للإجابة على الأسئلة العامة بدون ذكر كلية محددة
function answerGeneralQuestion(q) {
    let matchedField = null;

    for (const item of fieldMappings) {
        const normalizedSynonyms = item.synonyms.map(s =>
            s.toLowerCase().replace(/[؟?]/g, '')
        );

        if (normalizedSynonyms.some(syn => q.includes(syn))) {
            matchedField = item;
            break;
        }
    }

    if (!matchedField) {
        return "❌ لم أتمكن من تحديد الكلية أو السؤال. يرجى ذكر اسم الكلية أو توضيح سؤالك.";
    }

    // إجابة عامة عن الحقل لجميع الكليات
    const responses = colleges.map(college => {
        const value = college[matchedField.field];
        return `${matchedField.label} في ${college.name_ar}: ${value}`;
    });

    return responses.join('\n\n');
}

// دالة لإنشاء الردود
function generateResponse(colleges, field) {
    return colleges.map(college => {
        const value = college[field.field];
        const collegeName = college.name_ar;

        if (typeof value === 'boolean') {
            return value ?
                `✅ نعم، ${field.label} متوفر في ${collegeName}` :
                `❌ لا، ${field.label} غير متوفر في ${collegeName}`;
        }

        if (field.field.includes('satisfaction') || field.field.includes('rate') ||
            field.field.includes('quality')) {
            return `${field.label} في ${collegeName}: ${value}%`;
        }

        if (field.field === 'research_funding') {
            const formatted = new Intl.NumberFormat('ar-SA').format(value);
            return `${field.label} في ${collegeName}: ${formatted} ريال`;
        }

        return `${field.label} في ${collegeName}: ${value}`;
    }).join('\n\n');
}

